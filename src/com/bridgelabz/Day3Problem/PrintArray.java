package com.bridgelabz.Day3Problem;

public class PrintArray {
    public static void main(String[] args) {
        //Initialize array
        int [] arr = new int [] {1, 2, 3, 4, 5};
        System.out.println("Elements of given array: ");

        for (int j : arr) {
            System.out.print(j + " ");
        }
    }

}
